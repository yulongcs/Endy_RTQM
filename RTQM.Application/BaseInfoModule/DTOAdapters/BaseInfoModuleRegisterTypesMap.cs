using Lgsoft.RTQM.Application.BaseInfoModule.DTOAdapters.Maps;
using Lgsoft.SF.Infrastructure.CrossCutting.Adapters;

namespace Lgsoft.RTQM.Application.BaseInfoModule.DTOAdapters
{
    public class BaseInfoModuleRegisterTypesMap : RegisterTypesMap
    {
        public BaseInfoModuleRegisterTypesMap()
        {
            RegisterMap(new MaterialToMaterialDTOMap());
            RegisterMap(new MaterialEnumerableToMaterialDTOListMap());
            RegisterMap(new SupplierToSupplierDTOMap());
            RegisterMap(new SupplierEnumerableToSupplierDTOListMap());
        }
    }
}
