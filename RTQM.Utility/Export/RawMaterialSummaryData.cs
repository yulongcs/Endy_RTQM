namespace Lgsoft.RTQM.Utility.Export
{
    /// <summary>
    /// 原材料汇总数据。
    /// </summary>
    class RawMaterialSummaryData
    {
        /// <summary>
        /// 汇总数据名称。
        /// </summary>
        public string SummaryName { get; set; }

        /// <summary>
        /// 来料总数汇总。
        /// </summary>
        public int TotalSummary { get; set; }

        /// <summary>
        /// 入库总数汇总。
        /// </summary>
        public int QtyTotalSummary { get; set; }

        /// <summary>
        /// 让步数量汇总。
        /// </summary>
        public int ConcessionCountSummary { get; set; }

        /// <summary>
        /// 拒绝数量汇总。
        /// </summary>
        public int RejectionCountSummary { get; set; }

        /// <summary>
        /// 返工数量汇总。
        /// </summary>
        public int ReworkCountSummary { get; set; }

        /// <summary>
        /// 报废数量汇总。
        /// </summary>
        public int ScrapCountSummary { get; set; }
    }
}
